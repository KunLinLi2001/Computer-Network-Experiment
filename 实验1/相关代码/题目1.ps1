ping -l 1024 -n 20 www.163.com
# 设置发送数据包大小1024字节,回送请求个数50
ping -l 1024 -n 50 www.163.com
# 设置发送数据包大小1024字节,回送请求个数100
ping -l 1024 -n 100 www.163.com